					<font color=red>Cara berpartisipasi:</font><br>
					- Setiap Guild memiliki seorang 'Verificator' yg bertugas untuk melakukan verifikasi CD pada komputer di ujung ruang war (disebut E-Room).<br>
					- Ketua Guild dapat menunjuk salah satu anggotanya untuk menjadi 'Verificator'. Caranya: Buka jendela guild (shortcut G), kemudian pilih nama anggota yg ingin ditunjuk, lalu klik tombol "Assist Senior", kemudian centang "Authentication" dan klik OK, seperti pada gambar dibawah.<br><img src="images/guide/Verificator.jpg" /><br>
					<i>Ketua Guild akan menjadi 'Verificator' secara otomatis apabila tidak ada satupun anggotanya yg ditunjuk.</i><br>
					<br>
					<font color=red>Map Club War:</font><br>
					Entrance.<br><img src="images/guide/EroomEntrance.jpg" /><br>
					<br>
					E-Room.<br><img src="images/guide/EroomTerminal.jpg" /><br>
					<font color=red>Tugas:</font><br>
					- Verificator membutuhkan CD untuk melakukan verifikasi. Verifikasi harus dilakukan untuk menguasai E-Room tersebut. CD dapat dibeli pada NPC Nurse didepan pintu masuk E-Room (lihat map entrance).<br>
					- Masuk ke dalam E-Room, terdapat "Terminal Computer", di ujung ruang. Verificator harus mem-verifikasi computer tersebut menggunakan CD.<br>
					- Ketika verifikasi dimulai, akan muncul proses verifikasi. Verificator <font color=red>tidak boleh bergerak atau mati</font> selama proses verifikasi berlangsung. Potion dapat digunakan ketika proses verifikasi berlangsung, dan tidak akan membatalkan proses verifikasi.<br>
					- Setelah verifikasi berhasil, seluruh pemain dari guild lain akan keluar dari ruangan. Guild yg berhasil mem-verifikasi akan menjadi penguasa sementara hingga Club War berakhir.<br>
					- Jika Guild penguasa sementara sanggup mempertahankan E-Room tersebut (computer tidak diverifikasi lagi oleh guild lain), maka Guild tersebut akan menjadi penguasa area E-Room hingga periode Club War yg akan datang!<br>
					<br>
					<font color=red>Ketentuan:</font><br>
					- Club War berlangsung selama 60 menit.<br>
					- Guild pemenang akan menjadi penguasa suatu area hingga Club War yg akan datang.<br>
					- Ketua dari Guild penguasa dapat mengatur pajak area untuk wilayah yg dikuasainya (pajak default adalah 5%).<br>Gold akan di dapat dari pajak, dan masuk ke dalam Storage Guild (storage khusus di dalam E-Room). Ketua Guild dapat mengambil Gold hasil pajak dalam storage tersebut.<br>
					- Ketua Guild memiliki akses Storage yg ada didalam E-Room. Ini adalah Storage penyimpanan extra, dan hanya dapat diakses di dalam E-Room.<br>
					- Seluruh anggota Guild penguasa dapat masuk ke dalam E-Room yg dikuasai.<br>
					- Equip tidak akan jatuh dan EXP tidak akan turun di dalam map E-Room (dalam ruangan).<br>
					- PET tidak dapat digunakan di dalam map E-Room (dalam ruangan).<br>
					- DESTINY BOX tidak dapat digunakan di dalam map E-Room (dalam ruangan).
